				<p style="text-align:justify;">A MaDAS project is built with three components: <b>Data Source</b>, <b>DAS servers</b>, and <b>Visualization</b> plugins to display and analyze the annotations.</p>
				
				<p style="text-align:justify;"><b>Data source</b> plugins allows users to upload and manage a set of annotations. We can use these plugins for example to upload a GFF file, add an external DAS track, upload a FASTA file or load data from a gene expression experiment. The annotations uploaded with these plugins are stored in the project and can be analyzed using the visualization plugins or retrieved using standard DAS queries.</p>
				<p style="text-align:justify;"><b>DAS server</b> plugins are the heart of MaDAS. Currently, we have only developed  the common das server plugin which deploy all the data stored in MaDAS using the DAS 1.5 standard.</p>
				<p style="text-align:justify;"><b>Visualization</b> plugins allow users to view and analyze the annotations through several intuitive interfaces.  There are general plugins like Madasmap that can represent several kinds of annotations but also we have specific plugins that can display only data for certain kinds of experiments as the Gene Expression plugin. </p>
				<p style="text-align:justify">In the near future, people will be able to design and upload their own plugins based on the MaDAS plugin specifications. In a meantime if you have another type of data that you want to include in MaDAS please feel free to <a href="mailto:vdelatorre@cnio.es" title="mail to vdelatorre@cnio.es">contact us</a></p>
