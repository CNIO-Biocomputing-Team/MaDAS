	<div id="1"  class="hidden">
		<p style="text-align:justify;">MaDAS is a <b>collaborative sequence annotation tool</b>.  Composed of sophisticated plugins, MaDAS allows users to <b>upload</b>, <b>deploy</b> and <b>analyze</b> their data through a user-friendly interface.</p>
		
		 <p style="text-align:justify;">Annotations in MaDAS are stored in projects, the project leader can decide on the roles for people involved in his project.  Annotations from <b>different sources</b> can be <b>compared</b> and <b>edited</b> with a <b>version control system</b> keeping track of every change in the annotations.  These annotations are also deployed using <b>DAS format</b>.</p>
	</div>
	
	<div id="2" style="display:none;" class="hidden">
		<p style="text-align:justify;">A MaDAS project is built with three components: <b>Data Source</b>, <b>DAS servers</b>, and <b>Visualization</b> plugins to display and analyze the annotations.</p>
		<p style="text-align:justify;">These components are designed based on the requirements of the different kinds of data that can be analyzed. </p>
		<p style="text-align:justify;"><b>Data source</b> plugins allows users to upload and manage a set of annotations. We could use these plugins for example to upload a GFF file, add an external DAS track, upload a FASTA file or load data from a gene expression experiment. The annotations uploaded with these plugins are stored inside the project and could be analyzed using the visualization plugins or retrieved using standard DAS queries.</p>
		<p style="text-align:justify;"><b>DAS server</b> plugins are the heart of MaDAS. Currently, we have only developed  the common das server plugin which deploys all the data stored in MaDAS using the DAS 1.5 standard.</p>
		<p style="text-align:justify;"><b>Visualization</b> plugins allow users to view and analyze the annotations through several intuitive interfaces.  There are general plugins like Madasmap that could represent several kinds of annotations but also we have specific plugins that can display only data for certain kinds of experiments as the Gene Expression plugin. </p>
	</div>
	
	<div id="3" style="display:none;" class="hidden">
		<p style="text-align:justify;">Annotations in MaDAS are stored inside projects. A <b>MaDAS project</b> is very similar as a real annotation project. Is composed by different people who have different roles. There are three roles: <b>Project leader</b>, <b>Annotator</b>, and <b>Researcher</b>.</p>
		<p style="text-align:justify;">The <b>Project leader</b> decides the role for all the people that work in the project and also has all the privileges to manage the annotations inside his project.</p>
		<p style="text-align:justify;">An <b>Annotator</b> has the privileges to use the data source and visualization plugins but he can't delete annotations.</p>
		<p style="text-align:justify;">An <b>Researcher</b> could use only the visualization plugins to get conclusions about the data stored in the project.</p>
	</div>
	
	<div id="4" style="display:none;" class="hidden">
		<p style="text-align:justify;">The key concept in MaDAS is the <b>plug-in pipeline</b>:</p>
		<p style="text-align:justify;"><b>Data source plug-ins</b> -> <b>Das server plug-ins</b> -> <b>Visualization plug-ins</b></p>
		<p style="text-align:justify;">Annotations in MaDAS are loaded using the data source plug-ins. The Das servers then deploy these annotations and finally the annotations are analyzed by the visualization plug-ins.</p>
		<p style="text-align:justify;">As anonymous user you can only use the visualization plug-ins. You need to register in MaDAS to store your annotations using the data source plug-ins.</p>
		<p style="text-align:justify;">Registration is needed as the only way to keep track of the changes in the annotations.</p>
	</div>

	
